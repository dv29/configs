  local ft = vim.bo.filetype
  -- if (ft == 'json') then
  --   vim.cmd('Prettier')
  -- end

  if (ft == 'javascript') then
    vim.cmd('EslintFixAll')
    return
  end

  if (ft == 'typescript') then
    vim.cmd('EslintFixAll')
    return
  end

  vim.lsp.buf.format {
    async = true,
    -- filter = function(client) return client.name ~= "tsserver" end
  }
end)

